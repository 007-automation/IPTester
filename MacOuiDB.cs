    /// <summary>根据 MAC 返回厂商名称，无匹配返回 null</summary>
    public static string? Lookup(string? mac)
    {
        if (string.IsNullOrWhiteSpace(mac) || mac == "-") return null;
        // 取前8字符(不含分隔符)匹配 OUI
        var clean = mac.Replace("-", "").Replace(":", "").Replace(".", "").ToUpperInvariant();
        if (clean.Length < 6) return null;
        var oui = clean[..6];
        var formatted = $"{oui[..2]}-{oui[2..4]}-{oui[4..6]}";
        return _oui.TryGetValue(formatted, out var vendor) ? vendor : null;
    }
